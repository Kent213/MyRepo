package FolderOfStuff;

public class caseSwitch {
    public static void main (String[] args){
        int n = 2;
        switch(n) {
            case 1:
            System.out.println("nay");
            break;
            case 2:
            System.out.println("yay");
            break;
            case 3:
            System.out.println("nayy");
            break;
        }
    }
}
